'use client';

import { useState } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import {
    Trophy,
    Search,
    CheckCircle,
    XCircle,
    Eye,
    ChevronLeft,
    ChevronRight,
    Loader2,
    Clock,
    Calendar,
    MapPin,
    Users,
    IndianRupee,
    X,
    Check,
    AlertTriangle,
    Building2,
    Flag,
    Globe,
} from 'lucide-react';

// Types
interface PendingEvent {
    id: number;
    event_name: string;
    event_level_type_id: number;
    event_level: string;
    state_name: string;
    district_name: string;
    venue: string;
    event_date: string;
    reg_start_date: string;
    reg_end_date: string;
    event_fees: number;
    association_name: string;
    contact_person: string;
    mobile_number: string;
    created_at: string;
}

// Mock data
const mockPendingEvents: PendingEvent[] = [
    { id: 15, event_name: 'Coimbatore District Championship 2025', event_level_type_id: 1, event_level: 'District Level', state_name: 'Tamil Nadu', district_name: 'Coimbatore', venue: 'Nehru Stadium, Coimbatore', event_date: '2025-03-15', reg_start_date: '2025-02-15', reg_end_date: '2025-03-10', event_fees: 500, association_name: 'Coimbatore DSA', contact_person: 'Rajesh Kumar', mobile_number: '9876543210', created_at: '2025-01-25' },
    { id: 16, event_name: 'Mysuru District Championship 2025', event_level_type_id: 1, event_level: 'District Level', state_name: 'Karnataka', district_name: 'Mysuru', venue: 'District Sports Complex, Mysuru', event_date: '2025-04-10', reg_start_date: '2025-03-10', reg_end_date: '2025-04-05', event_fees: 500, association_name: 'Mysuru DSA', contact_person: 'Ramesh K', mobile_number: '9988776655', created_at: '2025-01-24' },
    { id: 17, event_name: 'Kerala State Championship 2025', event_level_type_id: 2, event_level: 'State Level', state_name: 'Kerala', district_name: 'Thiruvananthapuram', venue: 'Greenfield Stadium, Trivandrum', event_date: '2025-05-20', reg_start_date: '2025-04-15', reg_end_date: '2025-05-15', event_fees: 800, association_name: 'KSFI', contact_person: 'Suresh Nair', mobile_number: '8877665544', created_at: '2025-01-23' },
];

const eventLevels = [
    { id: 1, name: 'District Level', icon: Building2, color: 'bg-blue-500/20 text-blue-400' },
    { id: 2, name: 'State Level', icon: Flag, color: 'bg-purple-500/20 text-purple-400' },
    { id: 3, name: 'National Meet', icon: Globe, color: 'bg-amber-500/20 text-amber-400' },
];

export default function EventApprovalsPage() {
    const [events, setEvents] = useState<PendingEvent[]>(mockPendingEvents);
    const [searchQuery, setSearchQuery] = useState('');
    const [currentPage, setCurrentPage] = useState(1);
    const [isLoading, setIsLoading] = useState(false);
    const [viewingEvent, setViewingEvent] = useState<PendingEvent | null>(null);
    const [processingId, setProcessingId] = useState<number | null>(null);
    const [rejectReason, setRejectReason] = useState('');
    const [showRejectModal, setShowRejectModal] = useState(false);
    const [selectedForReject, setSelectedForReject] = useState<PendingEvent | null>(null);
    const itemsPerPage = 10;

    // Filter events
    const filteredEvents = events.filter(event =>
        event.event_name.toLowerCase().includes(searchQuery.toLowerCase()) ||
        event.venue.toLowerCase().includes(searchQuery.toLowerCase()) ||
        event.association_name.toLowerCase().includes(searchQuery.toLowerCase())
    );

    // Pagination
    const totalPages = Math.ceil(filteredEvents.length / itemsPerPage);
    const paginatedEvents = filteredEvents.slice(
        (currentPage - 1) * itemsPerPage,
        currentPage * itemsPerPage
    );

    const handleApprove = async (event: PendingEvent) => {
        setProcessingId(event.id);
        await new Promise(resolve => setTimeout(resolve, 1000));
        setEvents(prev => prev.filter(e => e.id !== event.id));
        setProcessingId(null);
        setViewingEvent(null);
    };

    const handleReject = async () => {
        if (!selectedForReject) return;
        setProcessingId(selectedForReject.id);
        await new Promise(resolve => setTimeout(resolve, 1000));
        setEvents(prev => prev.filter(e => e.id !== selectedForReject.id));
        setProcessingId(null);
        setShowRejectModal(false);
        setSelectedForReject(null);
        setRejectReason('');
        setViewingEvent(null);
    };

    const openRejectModal = (event: PendingEvent) => {
        setSelectedForReject(event);
        setShowRejectModal(true);
    };

    const getEventLevelBadge = (levelId: number) => {
        const level = eventLevels.find(l => l.id === levelId);
        if (!level) return null;
        const Icon = level.icon;
        return (
            <span className={`inline-flex items-center gap-1 px-2 py-1 ${level.color} text-xs font-medium rounded-full`}>
                <Icon className="w-3 h-3" />
                {level.name}
            </span>
        );
    };

    const formatCurrency = (amount: number) =>
        new Intl.NumberFormat('en-IN', { style: 'currency', currency: 'INR', maximumFractionDigits: 0 }).format(amount);

    return (
        <div className="p-6 space-y-6">
            {/* Header */}
            <div className="flex flex-col md:flex-row md:items-center md:justify-between gap-4">
                <div>
                    <h1 className="text-2xl font-bold text-white">Event Approvals</h1>
                    <p className="text-slate-400 mt-1">Review and approve pending event requests</p>
                </div>
                <div className="flex items-center gap-2">
                    <div className="px-4 py-2 bg-amber-500/20 text-amber-400 rounded-lg flex items-center gap-2">
                        <Clock className="w-4 h-4" />
                        <span className="font-medium">{events.length} Pending</span>
                    </div>
                </div>
            </div>

            {/* Search */}
            <div className="relative max-w-md">
                <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-5 h-5 text-slate-500" />
                <input
                    type="text"
                    value={searchQuery}
                    onChange={(e) => setSearchQuery(e.target.value)}
                    placeholder="Search events..."
                    className="w-full pl-10 pr-4 py-2.5 bg-slate-800/50 border border-slate-700 rounded-lg text-white placeholder-slate-500 focus:outline-none focus:ring-2 focus:ring-blue-500/50"
                />
            </div>

            {/* Events Grid */}
            <div className="grid md:grid-cols-2 lg:grid-cols-3 gap-4">
                {isLoading ? (
                    <div className="col-span-full py-12 text-center">
                        <Loader2 className="w-8 h-8 text-blue-400 animate-spin mx-auto" />
                    </div>
                ) : paginatedEvents.length === 0 ? (
                    <div className="col-span-full py-12 text-center text-slate-400">
                        <CheckCircle className="w-12 h-12 mx-auto mb-2 text-green-400 opacity-50" />
                        <p>No pending event approvals</p>
                    </div>
                ) : (
                    paginatedEvents.map((event, index) => (
                        <motion.div
                            key={event.id}
                            initial={{ opacity: 0, y: 20 }}
                            animate={{ opacity: 1, y: 0 }}
                            transition={{ delay: index * 0.05 }}
                            className="bg-slate-800/50 rounded-xl border border-slate-700/50 overflow-hidden"
                        >
                            <div className="relative h-28 bg-gradient-to-br from-blue-500/20 to-purple-500/20 p-4">
                                <div className="absolute top-4 left-4">
                                    {getEventLevelBadge(event.event_level_type_id)}
                                </div>
                                <div className="absolute bottom-4 left-4 right-4">
                                    <h3 className="text-lg font-bold text-white line-clamp-2">{event.event_name}</h3>
                                </div>
                            </div>

                            <div className="p-4 space-y-3">
                                <div className="flex items-center gap-2 text-sm text-slate-400">
                                    <Calendar className="w-4 h-4" />
                                    <span>{new Date(event.event_date).toLocaleDateString('en-IN', { year: 'numeric', month: 'short', day: 'numeric' })}</span>
                                </div>
                                <div className="flex items-center gap-2 text-sm text-slate-400">
                                    <MapPin className="w-4 h-4" />
                                    <span className="truncate">{event.venue}</span>
                                </div>
                                <div className="flex items-center gap-2 text-sm text-slate-400">
                                    <IndianRupee className="w-4 h-4" />
                                    <span>Entry Fee: {formatCurrency(event.event_fees)}</span>
                                </div>
                                <div className="pt-3 border-t border-slate-700/50 text-sm">
                                    <p className="text-slate-400">Organized by</p>
                                    <p className="text-white">{event.association_name}</p>
                                </div>
                            </div>

                            <div className="p-4 pt-0 flex gap-2">
                                <button
                                    onClick={() => setViewingEvent(event)}
                                    className="flex-1 py-2 bg-slate-700/50 text-slate-300 rounded-lg hover:bg-slate-700 text-sm font-medium flex items-center justify-center gap-1"
                                >
                                    <Eye className="w-4 h-4" /> View
                                </button>
                                <button
                                    onClick={() => handleApprove(event)}
                                    disabled={processingId === event.id}
                                    className="flex-1 py-2 bg-green-500/20 text-green-400 rounded-lg hover:bg-green-500/30 text-sm font-medium flex items-center justify-center gap-1 disabled:opacity-50"
                                >
                                    {processingId === event.id ? <Loader2 className="w-4 h-4 animate-spin" /> : <Check className="w-4 h-4" />}
                                    Approve
                                </button>
                                <button
                                    onClick={() => openRejectModal(event)}
                                    className="py-2 px-3 bg-red-500/20 text-red-400 rounded-lg hover:bg-red-500/30 text-sm font-medium"
                                >
                                    <X className="w-4 h-4" />
                                </button>
                            </div>
                        </motion.div>
                    ))
                )}
            </div>

            {/* Pagination */}
            {totalPages > 1 && (
                <div className="flex items-center justify-center gap-2">
                    <button onClick={() => setCurrentPage(p => Math.max(1, p - 1))} disabled={currentPage === 1} className="p-2 bg-slate-700 rounded-lg text-slate-400 hover:text-white disabled:opacity-50">
                        <ChevronLeft className="w-4 h-4" />
                    </button>
                    <span className="text-slate-400 text-sm px-3">Page {currentPage} of {totalPages}</span>
                    <button onClick={() => setCurrentPage(p => Math.min(totalPages, p + 1))} disabled={currentPage === totalPages} className="p-2 bg-slate-700 rounded-lg text-slate-400 hover:text-white disabled:opacity-50">
                        <ChevronRight className="w-4 h-4" />
                    </button>
                </div>
            )}

            {/* View Detail Modal */}
            <AnimatePresence>
                {viewingEvent && (
                    <motion.div
                        initial={{ opacity: 0 }}
                        animate={{ opacity: 1 }}
                        exit={{ opacity: 0 }}
                        className="fixed inset-0 bg-black/60 backdrop-blur-sm z-50 flex items-center justify-center p-4"
                        onClick={() => setViewingEvent(null)}
                    >
                        <motion.div
                            initial={{ scale: 0.95, opacity: 0 }}
                            animate={{ scale: 1, opacity: 1 }}
                            exit={{ scale: 0.95, opacity: 0 }}
                            className="bg-slate-800 rounded-2xl max-w-2xl w-full max-h-[90vh] overflow-y-auto"
                            onClick={(e) => e.stopPropagation()}
                        >
                            <div className="relative h-40 bg-gradient-to-br from-blue-500/20 to-purple-500/20 p-6">
                                <button onClick={() => setViewingEvent(null)} className="absolute top-4 right-4 p-2 bg-slate-900/50 rounded-lg text-white hover:bg-slate-900/70">
                                    <X className="w-5 h-5" />
                                </button>
                                <div className="absolute bottom-6 left-6 right-6">
                                    {getEventLevelBadge(viewingEvent.event_level_type_id)}
                                    <h2 className="text-2xl font-bold text-white mt-2">{viewingEvent.event_name}</h2>
                                </div>
                            </div>

                            <div className="p-6 space-y-6">
                                <div className="grid grid-cols-2 gap-4">
                                    <div className="bg-slate-700/30 rounded-xl p-4">
                                        <p className="text-sm text-slate-400 mb-1">Event Date</p>
                                        <p className="font-medium text-white">{new Date(viewingEvent.event_date).toLocaleDateString('en-IN', { weekday: 'long', year: 'numeric', month: 'long', day: 'numeric' })}</p>
                                    </div>
                                    <div className="bg-slate-700/30 rounded-xl p-4">
                                        <p className="text-sm text-slate-400 mb-1">Entry Fee</p>
                                        <p className="font-medium text-white">{formatCurrency(viewingEvent.event_fees)}</p>
                                    </div>
                                    <div className="bg-slate-700/30 rounded-xl p-4 col-span-2">
                                        <p className="text-sm text-slate-400 mb-1">Venue</p>
                                        <p className="font-medium text-white">{viewingEvent.venue}</p>
                                        <p className="text-sm text-slate-400">{viewingEvent.district_name}, {viewingEvent.state_name}</p>
                                    </div>
                                    <div className="bg-slate-700/30 rounded-xl p-4">
                                        <p className="text-sm text-slate-400 mb-1">Registration Period</p>
                                        <p className="font-medium text-white text-sm">
                                            {new Date(viewingEvent.reg_start_date).toLocaleDateString('en-IN')} - {new Date(viewingEvent.reg_end_date).toLocaleDateString('en-IN')}
                                        </p>
                                    </div>
                                    <div className="bg-slate-700/30 rounded-xl p-4">
                                        <p className="text-sm text-slate-400 mb-1">Association</p>
                                        <p className="font-medium text-white">{viewingEvent.association_name}</p>
                                    </div>
                                    <div className="bg-slate-700/30 rounded-xl p-4 col-span-2">
                                        <p className="text-sm text-slate-400 mb-1">Contact Person</p>
                                        <p className="font-medium text-white">{viewingEvent.contact_person}</p>
                                        <p className="text-sm text-slate-400">{viewingEvent.mobile_number}</p>
                                    </div>
                                </div>

                                <div className="flex gap-3 pt-4 border-t border-slate-700">
                                    <button
                                        onClick={() => handleApprove(viewingEvent)}
                                        disabled={processingId === viewingEvent.id}
                                        className="flex-1 py-3 bg-green-500 text-white rounded-lg hover:bg-green-600 font-medium flex items-center justify-center gap-2 disabled:opacity-50"
                                    >
                                        {processingId === viewingEvent.id ? <Loader2 className="w-5 h-5 animate-spin" /> : <><Check className="w-5 h-5" /> Approve Event</>}
                                    </button>
                                    <button
                                        onClick={() => openRejectModal(viewingEvent)}
                                        className="flex-1 py-3 bg-red-500/20 text-red-400 rounded-lg hover:bg-red-500/30 font-medium flex items-center justify-center gap-2"
                                    >
                                        <X className="w-5 h-5" /> Reject
                                    </button>
                                </div>
                            </div>
                        </motion.div>
                    </motion.div>
                )}
            </AnimatePresence>

            {/* Reject Modal */}
            <AnimatePresence>
                {showRejectModal && selectedForReject && (
                    <motion.div
                        initial={{ opacity: 0 }}
                        animate={{ opacity: 1 }}
                        exit={{ opacity: 0 }}
                        className="fixed inset-0 bg-black/60 backdrop-blur-sm z-50 flex items-center justify-center p-4"
                        onClick={() => setShowRejectModal(false)}
                    >
                        <motion.div
                            initial={{ scale: 0.95, opacity: 0 }}
                            animate={{ scale: 1, opacity: 1 }}
                            exit={{ scale: 0.95, opacity: 0 }}
                            className="bg-slate-800 rounded-2xl max-w-md w-full"
                            onClick={(e) => e.stopPropagation()}
                        >
                            <div className="p-6">
                                <div className="w-12 h-12 bg-red-500/20 rounded-full flex items-center justify-center mx-auto mb-4">
                                    <AlertTriangle className="w-6 h-6 text-red-400" />
                                </div>
                                <h3 className="text-xl font-bold text-white text-center mb-2">Reject Event</h3>
                                <p className="text-slate-400 text-center mb-6">
                                    Are you sure you want to reject <span className="text-white font-medium">{selectedForReject.event_name}</span>?
                                </p>
                                <div className="mb-6">
                                    <label className="block text-sm font-medium text-slate-400 mb-2">Reason for rejection</label>
                                    <textarea
                                        value={rejectReason}
                                        onChange={(e) => setRejectReason(e.target.value)}
                                        placeholder="Enter reason (optional)"
                                        rows={3}
                                        className="w-full px-4 py-3 bg-slate-700/50 border border-slate-600 rounded-lg text-white placeholder-slate-500 focus:outline-none focus:ring-2 focus:ring-red-500/50"
                                    />
                                </div>
                                <div className="flex gap-3">
                                    <button onClick={() => setShowRejectModal(false)} className="flex-1 py-2.5 bg-slate-700 text-white rounded-lg hover:bg-slate-600 font-medium">Cancel</button>
                                    <button onClick={handleReject} disabled={processingId === selectedForReject.id} className="flex-1 py-2.5 bg-red-500 text-white rounded-lg hover:bg-red-600 font-medium flex items-center justify-center gap-2 disabled:opacity-50">
                                        {processingId === selectedForReject.id ? <Loader2 className="w-5 h-5 animate-spin" /> : 'Reject'}
                                    </button>
                                </div>
                            </div>
                        </motion.div>
                    </motion.div>
                )}
            </AnimatePresence>
        </div>
    );
}
